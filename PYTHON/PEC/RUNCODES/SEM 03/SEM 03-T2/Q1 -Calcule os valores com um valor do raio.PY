PI = 3.141592
raio = float(input('').strip())
circunferencia = 2 * PI * raio
area_do_circulo  = PI * raio** 2
área_da_esfera = 4 * PI * raio ** 2
vol_da_esfera = 4/3 * PI * raio ** 3
print("%.6f"%(circunferencia))
print("%.6f"%(area_do_circulo))
print("%.6f"%(área_da_esfera))
print("%.6f"%(vol_da_esfera))
